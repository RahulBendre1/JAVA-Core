package kr.co.iamtek.java_tutorials.constant;

public class Constant {

	public static void main(String[] args) {
		//정수
		byte 	a1 = 123;
		short 	a2 = 12345;
		int 		a3 = 1234567890;
		long 	a4 = 1234567890123456789L;	//L
		System.out.println(a1);
		System.out.println(a2);
		System.out.println(a3);
		System.out.println(a4);
		
		//실수
		double b1 = 1.2345678901234567890;
		float 	b2 = 1.2345678901234567890F;	//F
		double b3 = 12345678901234567.890;
		System.out.println(b1);
		System.out.println(b2);
		System.out.println(b3);
		
	}

}

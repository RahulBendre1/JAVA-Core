package kr.co.iamtek.java_tutorials.operator;

public class Arithmetic {

	public static void main(String[] args) {
		int a;
		a = 1 + 2;
		System.out.println(a);		//3
		a = a - 1;
		System.out.println(a);		//2
		a = a * 2;
		System.out.println(a);		//4
		a = a / 2;
		System.out.println(a);		//2
		a = 10 % 7;
		System.out.println(a);		//3


	}

}

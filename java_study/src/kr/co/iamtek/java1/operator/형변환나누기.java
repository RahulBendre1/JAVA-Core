package kr.co.iamtek.java1.operator;

public class 형변환나누기 {

	public static void main(String[] args) {
        int a = 10;
        int b = 3;
        float c = 10.0f;
        float d = 3.0F;
        System.out.println(a/b);		//3
        System.out.println(c/d);		//3.333
        System.out.println(a/d);		//3.333
	}

}
